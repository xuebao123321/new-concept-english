// 按课号获取单词
export function getVocabularyByLesson(lessonNumber: number): VocabWord[] {
  return VOCABULARY.filter(v => v.lessonNumber === lessonNumber);
}

// 按课文组获取单词
export function getVocabularyByGroup(group: string): VocabWord[] {
  const lessonNums = group.split('-').slice(1).map(Number);
  return VOCABULARY.filter(v => lessonNums.includes(v.lessonNumber));
}
